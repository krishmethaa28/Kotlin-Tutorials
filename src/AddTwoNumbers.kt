fun main() {
    val num1 : Int = 23
    val num2 : Int = 46

    val total = num1 + num2
    val diff = num2 - num1
    val product = num1 * num2
    val ratio = num2 / num1

    println("The sum of ${num1} and ${num2} is ${total}" )
    println("The diff of ${num1} and ${num2} is ${diff}" )
    println("The product of ${num1} and ${num2} is ${product}" )
    println("The ratio of ${num1} and ${num2} is ${ratio}" )
}